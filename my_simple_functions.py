def add_numbers(a, b):
    return a + b

def subtract_numbers(a, b):
    return a - b

def multiply_numbers(a, b):
    return a * b

def divide_numbers(a, b):
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero!"

def multiply_many_numbers(a, b, c, d, e):
    return a*b*c*d*e

def add_one(a):
    return a + 1
    
